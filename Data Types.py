'''
   Data typesprint
                  '''
"""int"""                  
a=1234
print(a)
print(type(54))

# """float"""
b=6.3
print(b)
print(type(1.2))

# """str"""
c="hello"
print(c)
print(type('world'))

# """list"""
d=[1,2]
print(d)
print(type(['hii']))

# """bool"""
print(type(True))
print(type(False))
print(type(6<9))
print(type(4>1))

# """set"""
print(type({54}))
print(type({'hello'}))

# """dict"""
print(type({1:"python"}))
print(type({2:'java'}))

# """tuple"""
a=1,2,3
print(type(a))
b=5,7,2
print(type(b))

# """bytes"""
print(type(b"hello"))
print(type(b'world'))

# """bytearray"""
print(bytearray(b'hii'))
print(bytearray(b"kiran"))

# """complex"""
print(type(4+5j))
print(type(6+1j))